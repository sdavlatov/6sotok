-- ============================================================================
-- Миграция Neon: подтверждение email (Payload auth verify).
--
-- ПОРЯДОК ВАЖЕН, иначе существующие пользователи потеряют вход:
--   1) Выполнить ЭТОТ SQL (добавит колонки И пометит всех текущих юзеров
--      подтверждёнными).
--   2) ТОЛЬКО ПОСЛЕ этого добавить в Vercel env RESEND_API_KEY — это включит
--      верификацию. Если включить её до миграции, все уже существующие юзеры
--      (включая админа) не смогут войти: UnverifiedEmail.
--
-- Идемпотентно — повторный запуск безопасен.
-- ============================================================================

ALTER TABLE users ADD COLUMN IF NOT EXISTS _verified          boolean;
ALTER TABLE users ADD COLUMN IF NOT EXISTS _verificationtoken varchar;

-- Все, кто зарегистрировался ДО включения верификации, считаются подтверждёнными
-- (иначе вход заблокируется). Новые регистрации будут проходить проверку.
UPDATE users SET _verified = true WHERE _verified IS NULL;
